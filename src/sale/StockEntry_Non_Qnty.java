    private void save(){

        if(newItems.size()<1){
            JOptionPane.showMessageDialog(rootPane, "No Entry To Saved!");
            return;
        }
        try{
            //check exits..

            for(RowItem ri : newItems){
            if(con.executeQuery("select wt from raw_non_qnty where name='"+ri.name+"' and tounch="+ri.tounch).next()){
                con.executeUpdate("update raw_non_qnty set wt=wt+"+ri.wt+" where name='"+ri.name+"' and tounch="+ri.tounch);
                continue;
            }

                String qry = "insert into raw_non_qnty(name,wt,tounch) values('"+ri.name+"',"+ri.wt+","+ri.tounch+");";
//                System.out.println(qry);
                con.executeUpdate(qry);


                //update raw faulty stock...

                //Add to faulty..
                String fQty = "SELECT wt,rid FROM faultyraw where name='"+ri.name+"' and wt="+ri.wt+" and tounch="+ri.tounch+";";
                ResultSet fRs = con.createStatement().executeQuery(fQty);
                if(fRs.next()){
                    float wwtt_ff = fRs.getFloat(1);
                    int rid = fRs.getInt(2);
                    float newQnty = ri.wastage+wwtt_ff;
                    con.executeUpdate("update faultyraw set wt="+newQnty+" where rid = "+rid);
                }else{
                    con.executeUpdate("insert into faultyraw(name,tounch,wt,qnty) values('"+ri.name+"',"+ri.tounch+","+ri.wt+",1)");
                }
            }

            //Worker account...
             WorkerObj wo = (WorkerObj) jComboBox1.getSelectedItem();
            wo.gold_acc+=(totalFine);
            wo.money_acc+=(totalLabor);
            con.executeUpdate("update worker_ac set gold_ac="+wo.gold_acc+", money_ac="+wo.money_acc+" where wid="+wo.id);

            //System.out.println(qry);
            
            //remove all items..
            while(newItems.size()>0)
                newItems.remove(0);
            totalFine=0;
            totalLabor=0;
            JOptionPane.showMessageDialog(rootPane, "Stock Updated!");
            refreshTable();
        }catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, "Error:"+e);
        }
    }

