    private void addToTable(){
        String name = jTextField1.getText();
        String wt = jTextField2.getText();
        String wateg = jTextField7.getText();
        String tnch = jTextField4.getText();
        String lbr  = jTextField5.getText();
//        String qnty = jTextField3.getText();
        if(name.length()<1){
            JOptionPane.showMessageDialog(rootPane, "Please Enter Name");
            jTextField1.requestFocus();
            return;
        }
        if(wt.length()<1){
            JOptionPane.showMessageDialog(rootPane, "Please Enter Weight");
            jTextField2.requestFocus();
            return;
        }
        if(tnch.length()<1){
            JOptionPane.showMessageDialog(rootPane, "Please Enter Tounch");
            jTextField4.requestFocus();
            return;
        }

        if(lbr.length()<1){
            JOptionPane.showMessageDialog(rootPane, "Please Enter Labor");
            jTextField4.requestFocus();
            return;
        }

        if(wateg.length()<1){
            JOptionPane.showMessageDialog(rootPane, "Please Enter Faulty");
            jTextField4.requestFocus();
            return;
        }


        
        try{
            float wt_flt = Float.parseFloat(wt);
            float watg_flt = Float.parseFloat(wateg)/(float) 1000;

            RowItem ri = new RowItem(name,wt_flt, watg_flt);
            ri.wastage=watg_flt;
            totalFine += wt_flt+watg_flt;
            totalLabor += Float.parseFloat(lbr);
           // ri.qnty = Integer.parseInt(qnty);
            newItems.add(ri);
            jLabel10.setText(df_gold.format(totalFine));
            jLabel10.setText(df_mny.format(totalLabor));
        }catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, "Error:"+e);
            return;
        }
        refreshTable();
        reset();
    }

