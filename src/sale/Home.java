    private void jPanel1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jPanel1KeyTyped
        // Main Pnl Key
        char ch = evt.getKeyChar();
        switch(ch){
            case '1':
                 goSale();
                break;
            case '2':
                goCustomerAC();
                break;
            case '3':
                goStock();
                break;

            case '4':
                sellReturn();
                break;

            case '5':
                  new NewCustomer(this, true).setVisible(true);
                  break;

            case '6':
                goToStockEntry();
                break;
            case '7':
                gotoRawSell();
                break;
            case '8':
                gotoBinLakiEntry();
                break;
            case '9':
                gotoBinLakiSell();
                break;

            case 'P':
            case 'p':
                goToPhirti();
                break;

            case 'D':
            case 'd':
                productDetails();


        }
    }//GEN-LAST:event_jPanel1KeyTyped

