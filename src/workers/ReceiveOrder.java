    private void loadOrderDetails(String oid) throws Exception{
        DBConnection con = new DBConnection();        
        ResultSet rs = con.executeQuery("SELECT wid,name FROM order_to_worker_map natural join worker_mst where order_id="+oid);
        if(rs.next()){
            currentOID = oid;
            currentWid = rs.getInt(1);
            currentWname = rs.getString(2);
            orderDetails = new OrderDetails(currentWname, currentWid);
            jTextField2.setText(currentWname+" ("+currentWid+")");

            rs = con.executeQuery("SELECT count(*) FROM confermorder where order_id=" + oid);
            rs.next();
            int order_itm_count = rs.getInt(1);
            orderDetails.desings = new ProductDesing[order_itm_count];
            rs = con.executeQuery("SELECT productname,qnty,requirements FROM confermorder where order_id=" + oid);
            int i=0;
            while(rs.next()){
                orderDetails.desings[i] = new ProductDesing(0, rs.getString(1));
                orderDetails.desings[i].qnty = rs.getInt(2);
                 //orderDetails.desings[i].addRawMaterial(rs.getString(3));
                 if(orderDetails.desings[i].raw==null){
                        orderDetails.desings[i].loadMaterilaFromDB(currentOID);
                 }
             i++;
            }
            rs = con.executeQuery("SELECT count(*) FROM confermordermaterial where order_id=" + oid);
            rs.next();
            out_sourced_raw = new RowItem[rs.getInt(1)];
            used_out_sourced_raw = new RowItem[out_sourced_raw.length];
            i=0;
            rs = con.executeQuery("SELECT itemName,wt,tounch,qnty FROM confermordermaterial where order_id=" + oid);
            while(rs.next()){
                out_sourced_raw[i] = new RowItem(rs.getString(1), rs.getFloat(2), rs.getFloat(3));
                out_sourced_raw[i].qnty = rs.getInt(4);
                used_out_sourced_raw[i] = new RowItem(out_sourced_raw[i].name, out_sourced_raw[i].wt, out_sourced_raw[i].tounch);
                used_out_sourced_raw[i].qnty=0;
                i++;
            }


            System.out.println("****\nOrder To Be recieved!");
            for(RowItem ri : out_sourced_raw)
                System.out.println(""+ri.name+"::"+ri.qnty);
            System.out.println("*******");

            //display desing names..
    //        String[] dArr = new String[orderDetails.desings.length];
            //for(int i=0;i<dArr.length;i++)
             //   dArr[i] = orderDetails.desings[i].name;

            jComboBox1.setModel(new javax.swing.DefaultComboBoxModel(orderDetails.desings));

            jComboBox1.setSelectedIndex(0);
            jComboBox1ItemStateChanged(null);

            jComboBox1.requestFocus();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Sorry! Order Not Found");
        }
    }
