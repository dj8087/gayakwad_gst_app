    private void deleteSelectedRecord(){


        int index = jTable1.getSelectedRow();

        if(index<0){
            JOptionPane.showMessageDialog(rootPane, "Please Select Product!");
            return;
        }

        Product p = stock.stock.remove(index);
        refreshStockTable();
        finalLabor -= p.labor;
        jLabel17.setText("Rs."+finalLabor);
        currentRec--;
        jLabel7.setText(""+currentRec);
        setRecordingEnable(true);
    }

