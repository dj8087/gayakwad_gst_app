    private void addToStock(){
          try{
            Integer.parseInt(jTextField10.getText());
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Wrong Labor Entry", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }




         String details = "";
        for(RowItem ri : ((ProductDesing)jComboBox1.getSelectedItem()).raw){
            //Mani Plane 2456::17::70.50::2456.0&&...
            details+=ri.toDBString()+"&&";//name+"::"+ri.qnty+"::"+ri.wt+"&&";

            //deduct outsourced raw..
            for(int i=0;i<out_sourced_raw.length;i++){
                //RowItem o_ri = out_sourced_raw[i];
                int index = ri.name.indexOf(" ");
                if(index<1){
                    if(used_out_sourced_raw[i].name.toLowerCase().contains(ri.name.toLowerCase())){
                        //o_ri.qnty-=ri.qnty;
                        if(used_out_sourced_raw[i].wt==ri.wt && used_out_sourced_raw[i].tounch==ri.tounch){
                            used_out_sourced_raw[i].qnty+=ri.qnty;
                        }
                    }
                }else{
                    if(used_out_sourced_raw[i].name.toLowerCase().contains(ri.name.toLowerCase().substring(0, index))){
                        //o_ri.qnty-=ri.qnty;
                        if(used_out_sourced_raw[i].wt==ri.wt && used_out_sourced_raw[i].tounch==ri.tounch){
                            used_out_sourced_raw[i].qnty+=ri.qnty;
                        }
                    }
                }
            }
        }
        String barcode = jTextField4.getText();
        String name = ((ProductDesing) jComboBox1.getSelectedItem()).name;
        //float ws = Float.parseFloat(jTextField7.getText());
        float wt = Float.parseFloat(jTextField8.getText());
        //float cost = Float.parseFloat(jTextField8.getText());
        try{
            int ll = Integer.parseInt(jTextField10.getText());
            finalLabor+=ll;
            jLabel17.setText("Rs."+finalLabor);
            Product p = new Product(barcode, name, wt, details+"__"+currentWid);
            p.labor = ll;
            stock.addProduct(p);
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Wrong Labor Entry", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
       
        refreshStockTable();
        jTextField4.requestFocus();
        currentRec++;
        if(currentRec>(((ProductDesing) jComboBox1.getSelectedItem()).qnty)){
            jComboBox1.removeItemAt(jComboBox1.getSelectedIndex());
            
            if(jComboBox1.getModel().getSize()<1)
            {
                JOptionPane.showMessageDialog(rootPane, "Recording Complited! Please Select Finish.", "Finish", JOptionPane.INFORMATION_MESSAGE);
                jButton2.requestFocus();
                setRecordingEnable(false);
            }else{
                jComboBox1.setSelectedIndex(0);
                jComboBox1.requestFocus();
            }
        }
        else{
             jLabel7.setText(""+currentRec);
        }
        reset();
    }
