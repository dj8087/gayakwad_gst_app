    private void save(){
        try {
            String name = jTextField1.getText();
            String add = jTextField2.getText();
            String contact = jTextField4.getText();
            String gold_bal = jTextField5.getText();
            String money_bal = jTextField6.getText();
            
            String sr_sup = (String) jComboBox1.getSelectedItem();
            int ss = 0;
            if(sr_sup.equals("Yes"))
                ss=1;
            DBConnection con = new DBConnection();
            con.executeUpdate("insert into worker_mst(name,address,contact,raw_supplier) values('" + name + "','" + add + "','" + contact + "','"+ss+"')");
            ResultSet rs = con.executeQuery("select wid from worker_mst where name='" + name + "' and contact='"+contact+"'");
            rs.next();
            int wid = rs.getInt(1);
            con.executeUpdate("insert into worker_ac(wid,gold_ac,money_ac) values ('" + wid + "',"+gold_bal+","+money_bal+")");
            JOptionPane.showMessageDialog(rootPane,"Worker Added!","Done",JOptionPane.INFORMATION_MESSAGE);
            reset();
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, ex,"Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
    }
