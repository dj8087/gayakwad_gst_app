    private void initTable(){
         g_data = new Vector<GoldEntryData>();
        try{
        con = new DBConnection();
        ResultSet rs = con.executeQuery("Select * from gold_mst order by tounch");

        while(rs.next()){
            g_data.add(new GoldEntryData(rs.getInt(1), rs.getFloat(2), rs.getFloat(3)));
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, ""+e, "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        refreshTable();
        jPanel1.requestFocus();
    }
