    private void debit(){
        int index = jTable1.getSelectedRow();
        if(index<0){
            JOptionPane.showConfirmDialog(rootPane, "Please Select A Record");
            return;
        }
        GoldEntryData ged = g_data.get(index);
        try {
            con.executeUpdate("delete from gold_mst where idgold_mst=" + ged.id);
            JOptionPane.showMessageDialog(rootPane, "Gold Debited!", "Done", JOptionPane.INFORMATION_MESSAGE);
            initTable();
        } catch (Exception ex) {
           JOptionPane.showMessageDialog(rootPane, ex, "Error", JOptionPane.ERROR_MESSAGE);
           return;
        }
    }

     private void refreshTable(){
        String[][] data = new String[g_data.size()][];
        float totalWt =0,totalFine = 0;

        for(int i=0;i<data.length;i++)
        {
            data[i] = new String[2];
            GoldEntryData ged = g_data.get(i);
            data[i][0] = ""+ged.wt;
            data[i][1] = ""+ged.tounch;
            totalWt+=ged.wt;
            totalFine+= (ged.tounch/100.0f)*ged.wt;
        }
        jLabel6.setText("Total Weight:"+totalWt+" gm");
        jLabel7.setText("Total Fine:"+totalFine+" gm");

         jTable1.setModel(new javax.swing.table.DefaultTableModel(data,   new String [] {
                "Weight", "Tounch"
            }) {
            Class[] types = new Class [] {
                java.lang.String.class, java.lang.String.class
            };

            public Class getColumnClass(int columnIndex) {
                return types [columnIndex];
            }
        });


    }
