    private void save(){
        try {

               String name  = jTextField1.getText();
               String amt_t = jTextField2.getText();
               String cmnt = jTextField4.getText();

               float amt = 0;
               try{
                    amt = Float.parseFloat(amt_t);
               }catch(Exception e){
                   e.printStackTrace();
                   JOptionPane.showMessageDialog(rootPane, "Wrong Amount Entry");
                   return;
               }

            DBConnection con = new DBConnection();

            String qry = "";
            if(credit)
                qry="insert into aj_gold_credited (name,amount,comment) values ";
            else
                qry = "insert into aj_gold_debited (name,amount,comment) values ";

            qry += "('" + name + "'," + amt  +",'" + cmnt + "');";
                
            System.out.println(qry);
            con.executeUpdate(qry);
            if(credit)
                con.executeUpdate("update aj_gold_acc_m_g set money=money+"+amt);
            else
                con.executeUpdate("update aj_gold_acc_m_g set money=money-" + amt);

            JOptionPane.showMessageDialog(rootPane, "Account Updated!", "Done", JOptionPane.INFORMATION_MESSAGE);
            resetAll();
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, ex, "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }


   
