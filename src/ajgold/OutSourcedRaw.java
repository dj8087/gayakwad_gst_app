    public void loadRow(){
      r_data = new Vector<RowItem>();
        try{
        con = new DBConnection();
        ResultSet rs = con.executeQuery("SELECT order_id,itemName,wt,tounch,qnty FROM confermordermaterial where order_id not in (SELECT order_id FROM completedorder) order by order_id,itemName;");

        while(rs.next()){
            int oid = rs.getInt(1);
            RowItem ri = new RowItem(rs.getString(2), rs.getFloat(3),rs.getFloat(4));
            ri.qnty = rs.getInt(5);
            ri.order_id_if_out=oid;
            r_data.add(ri);
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, ""+e, "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        refreshTable();
        jTable1.requestFocus();
    }


