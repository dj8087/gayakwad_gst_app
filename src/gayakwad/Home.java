    private void jPanel1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jPanel1KeyTyped
        // Main Pnl Key
        char ch = evt.getKeyChar();
        switch(ch){
            case '1':
                 goToRawMaterial();
                break;
            case '2':
                goToWorkers();
                break;
            case '3':
                goToSaleSection();
                break;

            case '4':
                //acc
                goToAJAcc();
                break;

            case '5':
                        try {
                            setVisible(false);
                            new NewDesing(this).setVisible(true);
                        } catch (Exception ex) {
                            ex.printStackTrace();
                            JOptionPane.showMessageDialog(rootPane, ex);
                        }
                break;

            case '6':   //balance sheet entry
                new BalanceSheetEntry(this, true).setVisible(true);
                break;

            case '7':     //invoice
                showInvoices();
                break;


            case '8':     //invoice
                showSellReport();
                break;

                
        }
    }//GEN-LAST:event_jPanel1KeyTyped

