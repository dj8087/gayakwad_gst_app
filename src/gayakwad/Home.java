    private void showInvoices(){
       AllInvoices ai = new AllInvoices(this, false);
       
       String inv_no  = JOptionPane.showInputDialog("Enter Invoice Number:");
       if(inv_no.length()<1){
            ai.setVisible(true);
            ai.loadAllOpenInvoices();
        }
       else{
           if(inv_no.equalsIgnoreCase("c")){
                ai.setVisible(true);
                ai.loadAllClosedInvoices();
                return;
           }
           if(inv_no.equalsIgnoreCase("o")){
                ai.setVisible(true);
                ai.loadAllOpenInvoices();
                return;
           }
           try{
               int in = Integer.parseInt(inv_no);
                ai.setVisible(true);
                ai.openInvoice(in);
           }catch(Exception e){
               JOptionPane.showMessageDialog(rootPane, "Wrong Entry!");
           }
       }
    }
