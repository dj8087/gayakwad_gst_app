    private void jPanel1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jPanel1KeyTyped
        // Main Pnl Key
        char ch = evt.getKeyChar();
        switch(ch){
            case '1':
                new RowType(parent, true).setVisible(true);
                break;
            case '2':
                new RawStock(parent, true).setVisible(true);
                break;
            case '3':
                goToGoldSection();
                break;
            case '4':
                goToOldStock();
                break;
            case '5':
                goToStockEntry();
                break;
        }
}//GEN-LAST:event_jPanel1KeyTyped

