    Vector<RowItem> rowItems = new Vector<RowItem>();

    private void addToTable(){
        String name = jTextField1.getText();
        String wt  = jTextField2.getText();
        String tnch  = jTextField3.getText();
        String qntyS  = jTextField4.getText();
        String lbrS  = jTextField5.getText();
        String wasteg_txt=jTextField7.getText();
        String qnty_extra_txt =jTextField6.getText();

        


        float wttt = -1,tounch=-1,labor=0.0f,wasteg=0;
        int qnty=0,qnty_extra;
        try{
            wttt = Float.parseFloat(wt);
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Error", "Wrong Unit Weigth Entry", JOptionPane.ERROR_MESSAGE);
            return;
        }


        try{
            wasteg = Float.parseFloat(wasteg_txt);
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Error", "Wrong Unit Weigth Entry", JOptionPane.ERROR_MESSAGE);
            return;
        }

         try{
            tounch = Float.parseFloat(tnch);
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Error", "Wrong Tounch Entry", JOptionPane.ERROR_MESSAGE);
            return;
        }

         try{
            qnty = Integer.parseInt(qntyS);
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Error", "Wrong Quantity Entry", JOptionPane.ERROR_MESSAGE);
            return;
        }

          try{
            qnty_extra = Integer.parseInt(qnty_extra_txt);
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Error", "Wrong Quantity Entry", JOptionPane.ERROR_MESSAGE);
            return;
        }




         try{
            labor = Float.parseFloat(lbrS);
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Error", "Wrong Quantity Entry", JOptionPane.ERROR_MESSAGE);
            return;
        }
        RowItem ri = new RowItem(name, wttt,tounch);
        ri.qnty=qnty;
        ri.qnty_diff=qnty_extra;
        ri.wastage=wasteg;
        ri.labor=labor;
        ri.wid = ((WorkerObj) jComboBox1.getSelectedItem()).id;
        rowItems.add(ri);
        int finalQnty = qnty+qnty_extra;
        totalLobor+= (finalQnty*labor);
        totalFine+= (finalQnty*(wttt+wasteg))*(tounch/100);

        jLabel9.setText(""+df.format(totalLobor));
        if(totalFine<10000) //10g
            jLabel13.setText(""+df.format(totalFine));
        else
            jLabel13.setText(""+df.format(totalFine/1000.0f)+" g");
        refreshTable();
    }

    private void refreshTable(){
        String[][] data = new String[rowItems.size()][];
        for(int i=0;i<data.length;i++)
        {
            RowItem ri = rowItems.get(i);
            data[i] = new String[6];
            data[i][0] = ri.name;
            data[i][1] = ""+ri.wt+"+"+ri.wastage;
            data[i][2] = ""+ri.tounch;
            data[i][3] = ""+ri.qnty+"+"+ri.qnty_diff;
            data[i][4]=""+df.format((((ri.qnty+ri.qnty_diff)*(ri.wt+ri.wastage))*(ri.tounch/100))/1000);
            data[i][5] = ""+(ri.labor*(ri.qnty+ri.qnty_diff));            
        }
          jTable1.setModel(new javax.swing.table.DefaultTableModel(data,
            new String [] {
                "Name", "Unit Weight", "Tounch", "Quantity", "Fine (g)", "Labor"
            }
        ) {
            Class[] types = new Class [] {
                java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.String.class
            };
            boolean[] canEdit = new boolean [] {
                false, false, false, false, false, false
            };

            public Class getColumnClass(int columnIndex) {
                return types [columnIndex];
            }

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit [columnIndex];
            }
        });
    }
