    private void suggest(){
         int caretPos = jTextField1.getCaretPosition();
        String fullText = jTextField1.getText();

        String type = "";
        if(caretPos>1)
            type = fullText.substring(0,caretPos);
        else{
            //jTextField2.setText(type);
            return;
        }

        //search type in data..
        String found = null;
        for(String str : data){
            if(str.toLowerCase().indexOf(type.toLowerCase())==0){
                found = str;
                break;
            }
        }
        if(found==null){
            jTextField1.setText(type);
            return;
        }
       // if(type.length()>=found.length())   return;
        String sugge = found.substring(type.length());
        jTextField1.setText(type+sugge);
        jTextField1.setSelectionStart((type+sugge).length());
        jTextField1.setSelectionEnd(type.length());
        jTextField1.moveCaretPosition(type.length());
        //jTextField2.selectAll();;
    }
    
    private void finish(){
        try {
            DBConnection con = new DBConnection();
            
            for(RowItem ri : rowItems){
                //check exists..
                String check_q = "SELECT qnty,rec_id FROM raw_stock_mst where name='"+ri.name+"' and wt="+ri.wt+" and tounch="+ri.tounch+";";
                System.out.println(check_q);
                ResultSet rs_chk = con.executeQuery(check_q);
                if(rs_chk.next()){
                    //update
                    int qnty = rs_chk.getInt(1);
                    int rec_id = rs_chk.getInt(2);
                    int newQnty = qnty+ri.qnty;
                    con.executeUpdate("update raw_stock_mst set qnty="+newQnty+"  where rec_id="+rec_id);
                }else
                 {
                     int nextRec = 0;
                    ResultSet rs = con.executeQuery("select max(rec_id) from raw_stock_mst");
                    try{
                        rs.next();
                        nextRec =rs.getInt(1);
                    }catch(Exception e){}
                    nextRec++;
                    String qry = "insert into raw_stock_mst values('"
                                    +ri.name+"',"+ri.wt+","+ri.tounch+","
                                    +ri.qnty+","+ri.wid+","+nextRec+","
                                    +ri.wastage+","+ri.qnty_diff+","+ri.labor+");";
                    con.executeUpdate(qry);
                }



                //Add to faulty..
                String fQty = "SELECT qnty,rid FROM faultyraw where name='"+ri.name+"' and wt="+ri.wt+" and tounch="+ri.tounch+";";
                ResultSet fRs = con.createStatement().executeQuery(fQty);
                if(fRs.next()){
                    int qnty = fRs.getInt(1);
                    int rid = fRs.getInt(2);
                    int newQnty = ri.qnty_diff+qnty;
                    con.executeUpdate("update faultyraw set qnty="+newQnty+" where rid = "+rid);
                }else{
                    con.executeUpdate("insert into faultyraw(name,tounch,wt,qnty) values('"+ri.name+"',"+ri.tounch+","+ri.wt+","+ri.qnty_diff+")");
                }
            }
            //update workers a/c
            WorkerObj wo = (WorkerObj) jComboBox1.getSelectedItem();
            wo.gold_acc+=(totalFine);
            wo.money_acc+=(totalLobor);
            con.executeUpdate("update worker_ac set gold_ac="+wo.gold_acc+", money_ac="+wo.money_acc+" where wid="+wo.id);

            //System.out.println(qry);
            JOptionPane.showMessageDialog(rootPane, "Raw Material Updated!", "Done", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        } catch (Exception ex) {
            ex.printStackTrace();
           JOptionPane.showMessageDialog(rootPane, ""+ex, "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }
