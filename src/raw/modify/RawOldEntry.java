    private void deleteEntry() {
        int index = jTable1.getSelectedRow();
        if(index<0){
            JOptionPane.showMessageDialog(rootPane, "Please Select Entry");
            return;
        }
        String rec_id = data[index][0];
        float wt  = Float.parseFloat(data[index][3]);
        float tnch  = Float.parseFloat(data[index][4]);
        float qnty  = Float.parseFloat(data[index][5]);
        String wid =  data[index][6];
        float wastage  = Float.parseFloat(data[index][7]);
        float labor  = Float.parseFloat(data[index][8]);
        int qnty_diff  = Integer.parseInt(data[index][9]);
        //qnty+=qnty_diff;
        float fine = ((wt+wastage)*(tnch/100))*qnty;
        float total_labor = (labor*qnty);
        try {
            con.executeUpdate("delete from raw_stock_mst where rec_id=" + rec_id);
            //update worker_ac
            con.executeUpdate("update worker_ac set gold_ac=gold_ac-"+fine+", money_ac=money_ac-"+total_labor+" where wid=" + wid);
            System.out.println("totalFine:"+fine);
            System.out.println("totalLabor:"+total_labor);
            System.out.println("wid:"+data[index][6]);
            updateTable();
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, "Error"+ex);
            return;
        }
        
    }

    private void changeEntry() {
           int index = jTable1.getSelectedRow();
        if(index<0){
            JOptionPane.showMessageDialog(rootPane, "Please Select Entry");
            return;
        }
        String rec_id = data[index][0];
        try {
            TableModel tm = jTable1.getModel();
            String name = (String) tm.getValueAt(index, 2);
            String wt = (String)  tm.getValueAt(index, 3);
            String tnch =  (String) tm.getValueAt(index, 4);
            String qnty = (String)  tm.getValueAt(index, 5);
            con.executeUpdate("update raw_stock_mst set name='"+name+"', wt="+wt+", tounch="+tnch+",qnty="+qnty+" where rec_id=" + rec_id);
            updateTable();
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, "Error"+ex);
            return;
        }
    }

