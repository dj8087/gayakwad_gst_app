    private void updateTable(){
        try {
            con = new DBConnection();
            ResultSet rs = con.executeQuery("SELECT count(rec_id)  FROM raw_stock_mst r join worker_mst w where w.wid=r.wid;");
            rs.next();
            int count = rs.getInt(1);
            System.out.println(count);
            data = new String[count][];
            rs = con.executeQuery("SELECT rec_id,w.name,r.name,wt,tounch,qnty,w.wid,wastage,labor,qnty_diff FROM raw_stock_mst r join worker_mst w where w.wid=r.wid;");
            while(rs.next()){
                count--;
                data[count] = new String[10];
                for(int i=1;i<=data[count].length;i++)
                    data[count][i-1] = ""+rs.getObject(i);
            }


              jTable1.setModel(new javax.swing.table.DefaultTableModel(data,
            new String [] {
                "Rec. No.", "Worker", "Row", "Weigth","Tounch","Quantity"
            }
        ));

              
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, "Error:"+ex);
        }

    }
