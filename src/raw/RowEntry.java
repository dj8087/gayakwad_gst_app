    private void loadRawItems(){
        try {
            //load Row Itmes..
            con = new DBConnection();
            ResultSet rs;// = con.executeQuery("Select count(*) from design_mst");
            row = new Vector<RowItem>();
            rs = con.executeQuery("select * from row_mst");
            while (rs.next()) {
                row.add(new RowItem(rs.getString(1),rs.getFloat(2), rs.getFloat(3)));
            }
            String[] data = new String[row.size()];
            for (int i = 0; i < data.length; i++) {
                data[i] = row.get(i).name;
            }
            jComboBox1.setModel(new javax.swing.DefaultComboBoxModel(data));
            jComboBox1.setSelectedIndex(0);
            jComboBox1.requestFocus();
            jLabel8.setText("" + row.get(0).wt+" mg");
        } catch (Exception ex) {
            Logger.getLogger(RowEntry.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
