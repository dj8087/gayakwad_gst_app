    private void resetAll() throws Exception{
        
        jTextField4.setText("");
        jComboBox1.setSelectedIndex(0);
        jLabel8.setText("" + row.get(0).wt+" mg");
        while(material.size()>0)
            material.remove(0);

        refreshTable();
    }


    class RawEntry{
        public final String name;
        public final int qnty;
        public final float wt;


        public RawEntry(String name,float wt, int qnty) {
            this.name = name;
            this.qnty = qnty;
            this.wt=wt;
        }
    }

    Vector<RawEntry> material = new Vector<RawEntry>();

    private void addToTable(){
        String qnty = jTextField4.getText();
        int index = jComboBox1.getSelectedIndex();


        int qq = -1;
        try{
            qq = Integer.parseInt(qnty);
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Error", "Wrong Quantity Entry", JOptionPane.ERROR_MESSAGE);
            return;
        }
        material.add(new RawEntry(row.get(index).name,row.get(index).wt, qq));
        refreshTable();
        jComboBox1.requestFocus();
    }

    private void save(){

        
        for(RawEntry rm : material){
            try {

                if(con.executeQuery("select qnty from raw_Stock where name='"+rm.name+"'").next()){
                    String qry = "update raw_stock set qnty=qnty+"+rm.qnty+" where name='"+rm.name+"'";
                    System.out.println(qry);
                    con.executeUpdate(qry);
                }else{
                    String qry = "insert into raw_stock(name,qnty) values('"+rm.name+"',"+rm.qnty+");";
                    System.out.println(qry);
                    con.executeUpdate(qry);
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(rootPane, ex.toString(), "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        JOptionPane.showMessageDialog(rootPane, "Saved!", "Done", JOptionPane.INFORMATION_MESSAGE);
         reset();

    }
     private void reset(){
        jTextField4.setText("");
        jComboBox1.setSelectedIndex(0);
        jComboBox1.requestFocus();

        while(material.size()>0)
            material.remove(0);

        refreshTable();

        jComboBox1.requestFocus();
    }
     

    private void refreshTable(){
        String[][] data = new String[material.size()][];
        
        for(int i=0;i<data.length;i++)
        {
            RawEntry rm = material.get(i);
            data[i] = new String[3];
            data[i][0] = rm.name;
            data[i][0] = ""+rm.wt;
            data[i][1] = ""+rm.qnty;
        }
        
        //jLabel7.setText("Total Weight For Design:"+totalWt);
         jTable1.setModel(new javax.swing.table.DefaultTableModel(data,
            new String [] {
                "Name", "Weight","Quantity"
            }
        ) {
             Class[] types = new Class [] {
                java.lang.String.class, java.lang.String.class, java.lang.String.class
            };
            boolean[] canEdit = new boolean [] {
                false, false,false
            };

            public Class getColumnClass(int columnIndex) {
                return types [columnIndex];
            }

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit [columnIndex];
            }
        });
    }


